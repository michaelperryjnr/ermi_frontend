export function getDevotionalById(id: number): Devotional | undefined {
  return devotionals.find((devotional) => devotional.id === id);
}

export function getLatestDevotional(): Devotional {
  return devotionals[0];
}

export function getFilteredDevotionals(filters: {
  categories?: string[];
  author?: string;
  sortBy?: string;
}): Devotional[] {
  let filtered = [...devotionals];

  // Filter by categories
  if (filters.categories && filters.categories.length > 0) {
    filtered = filtered.filter((devotional) =>
      devotional.categories?.some((category) =>
        filters.categories?.includes(category)
      )
    );
  }

  // Filter by author
  if (filters.author && filters.author !== "All Authors") {
    filtered = filtered.filter(
      (devotional) => devotional.author === filters.author
    );
  }

  // Sort
  if (filters.sortBy) {
    switch (filters.sortBy) {
      case "newest":
        filtered.sort(
          (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
        );
        break;
      case "oldest":
        filtered.sort(
          (a, b) => new Date(a.date).getTime() - new Date(b.date).getTime()
        );
        break;
      case "a-z":
        filtered.sort((a, b) => a.title.localeCompare(b.title));
        break;
      case "z-a":
        filtered.sort((a, b) => b.title.localeCompare(a.title));
        break;
    }
  }

  return filtered;
}
